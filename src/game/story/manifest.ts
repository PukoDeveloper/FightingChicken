export const STORY_MENU_ENTRIES: StoryMenuEntry[] = [
  {
    number: '序章',
    title: '小雞的覺醒',
    subtitle: '一切從祖父留下的那句話開始',
    nodeId: 'prologue',
  },
  ...LEVEL_TITLES.map(([title, subtitle], idx) => {
    const level = idx + 1;
    return {
      number: `第 ${level} 關`,
      title,
      subtitle,
      nodeId: `ch1-${String(level).padStart(3, '0')}-pre`,
    };
  }),
];

export function storyNodeIdForLevel(level: number): string {
  return `ch1-${String(level).padStart(3, '0')}-pre`;
}

export function storyTitleForLevel(level: number): string | null {
  const entry = STORY_MENU_ENTRIES[level];
  return entry?.title ?? null;
}

export function storyNodeAfterBattle(level: number): string {
  const milestone = level > 0 && level % 5 === 0;
  if (milestone || level >= 50) {
    return `ch1-${String(level).padStart(3, '0')}-post`;
  }
  return storyNodeIdForLevel(level + 1);
}
